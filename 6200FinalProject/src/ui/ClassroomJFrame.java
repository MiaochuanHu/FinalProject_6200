    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String root = System.getProperty("user.dir");
        String FileName="classroom.csv"; //change the file name here
        String filePath = root+File.separator+"src"+File.separator+"csvFile"+File.separator+FileName;
        
        int selectedRowIndex = tblTeacher.getSelectedRow();
        if (selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Select a teacher first");
            return;
        }
        int selectedRowIndex2 = tblClassroom.getSelectedRow();
        if (selectedRowIndex2 < 0){
            JOptionPane.showMessageDialog(this, "Select a classroom first");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblTeacher.getModel();
        DefaultTableModel model2 = (DefaultTableModel) tblClassroom.getModel();
        
        String name = model.getValueAt(selectedRowIndex, 0).toString();
        
        if( model2.getValueAt(selectedRowIndex2, 1).toString().equals("Reserved")){
            JOptionPane.showMessageDialog(this, "Already Reserved, change a room!");
            return;
        }
        
        String A = model2.getValueAt(selectedRowIndex2, 0).toString();
        String B = "Reserved";
        String C = name;
         String data = A + "," + B + "," + C;
         try{
            Path path = Paths.get(filePath);
            List<String> lines = Files.readAllLines(path);
            lines.set(selectedRowIndex2, data);
            Files.write(path, lines);
            JOptionPane.showMessageDialog(this, "Reserved!");
        }catch(Exception e){
            e.printStackTrace();
        }
        
        populateClassroom();
    }//GEN-LAST:event_jButton1ActionPerformed

