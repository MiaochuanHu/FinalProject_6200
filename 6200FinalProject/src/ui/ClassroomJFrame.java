    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblTeacher.getSelectedRow();
        if (selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Select a teacher to view");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblTeacher.getModel();
        txtTeacherName1.setText(model.getValueAt(selectedRowIndex, 0).toString());
        
        int maxAge = 0;
        
        String root = System.getProperty("user.dir");
        String FileName="StudentAssign2Teacher.csv"; //change the file name here
        String filePath = root+File.separator+"src"+File.separator+"csvFile"+File.separator+FileName;
        String line = "";
        String splitBy = ",";
        
        try {
            BufferedReader br = new BufferedReader(new FileReader(filePath));
            while ((line = br.readLine()) != null){
                String[] user = line.split(splitBy);
                if (user[2].equals(txtTeacherName1.getText())){
                    if(Integer.parseInt(user[1]) > maxAge){
                        maxAge = Integer.parseInt(user[1]);
                    }
                }    
            }
            br.close();
        }catch(IOException es){
            es.printStackTrace();
        }
        
        if (maxAge != 0){
           txtAge.setText(Integer.toString(maxAge)); 
        }else{
            txtAge.setText("Null"); 
        }
        if (maxAge > 47){
           txtMaxGroup.setText("2"); 
        }else{
            txtMaxGroup.setText("3"); 
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

