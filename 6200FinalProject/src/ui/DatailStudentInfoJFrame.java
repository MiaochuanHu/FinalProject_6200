    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblStudentEnrollment.getSelectedRow();
        if (selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Select a student to edit");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblStudentEnrollment.getModel();
        String A = model.getValueAt(selectedRowIndex, 0).toString();
        String B = model.getValueAt(selectedRowIndex, 1).toString();
        String a = txtParent.getText();
        String b = txtAddress.getText();
        String c = txtPhone.getText();
        
        if (a.equals("") || b.equals("") || c.equals("")){
            JOptionPane.showMessageDialog(this, "Parent, Address, or Phone can't be empty");
            return;
        }
        String root = System.getProperty("user.dir");
        String FileName="studentsDetailedInfo.csv"; //change the file name here
        String fileToUpdate = root+File.separator+"src"+File.separator+"csvFile"+File.separator+FileName;
        
        String data = A + "," + B + "," + a + "," + b + "," + c;
        try{
            Path path = Paths.get(fileToUpdate);
            List<String> lines = Files.readAllLines(path);
            lines.set(selectedRowIndex, data);
            Files.write(path, lines);
            JOptionPane.showMessageDialog(this, "Info Edited");
        }catch(Exception e){
            e.printStackTrace();
        }
        populatetblStudentEnrollmentDetailed();
    }//GEN-LAST:event_btnEditActionPerformed

